-module(queuesk_pool_sup).
-behaviour(supervisor).

-export([start_link/0,
	 add_worker/1,
	 init/1]).

%%===================================================================
%% Supervisor API
%%===================================================================

%%--------------------------------------------------------------------
%% start_link
%%--------------------------------------------------------------------
start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%%--------------------------------------------------------------------
%% add_worker
%%--------------------------------------------------------------------
add_worker(Queue) ->
    supervisor:start_child(?MODULE, [Queue]).

%%===================================================================
%% Supervisor Callbacks
%%===================================================================

%%--------------------------------------------------------------------
%% init
%%--------------------------------------------------------------------
init([]) ->
    {ok, {{simple_one_for_one, 5, 10},
	  [
	   {queuesk_pool_worker,
	    {queuesk_pool_worker, start_link, []},
	    permanent,
	    3000,
	    worker,
	    [queuesk_pool_worker]}
	  ]}}.
