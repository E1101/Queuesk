-module(queuesk_pool_worker).

-export([start_link/0]).

%%===================================================================
%% API
%%===================================================================

%%--------------------------------------------------------------------
%% start_link
%%--------------------------------------------------------------------
start_link() ->
    {ok, spawn_link(fun loop/0)}.

loop() ->
    idle().

idle() ->
    receive
	touch ->
	    loop();
	_ ->
	    idle()
    end.
